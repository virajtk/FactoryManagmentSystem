package com.model;

public class Order {
	
	private String orderID;
	private String productType;
	private String orderDate;
	private String dayOfNeed;
	private String dayOfComplete;
	private String quantity;
	private String superviserID;
	private String color;
	private String transportType;
	private String location;
	private String remark;
	
	
	public String getOrderID() {
		return orderID;
	}
	public void setOrderID(String orderID) {
		this.orderID = orderID;
	}
	
	public String getProductType() {
		return productType;
	}
	public void setProductType(String productType) {
		this.productType = productType;
	}
	
	public String getOrderDate() {
		return orderDate;
	}
	public void setOrderDate(String orderDate) {
		this.orderDate = orderDate;
	}
	
	public String getDayOfNeed() {
		return dayOfNeed;
	}
	public void setDayOfNeed(String dayOfNeed) {
		this.dayOfNeed = dayOfNeed;
	}
	
	public String getQuantity() {
		return quantity;
	}
	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}
	
	public String getSuperviserID() {
		return superviserID;
	}
	public void setSuperviserID(String superviserID) {
		this.superviserID = superviserID;
	}
	
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	
	public String getTransportType() {
		return transportType;
	}
	public void setTransportType(String transportType) {
		this.transportType = transportType;
	}
	
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getDayOfComplete() {
		return dayOfComplete;
	}
	public void setDayOfComplete(String dayOfComplete) {
		this.dayOfComplete = dayOfComplete;
	}
	
	
	
	
	
	
}
